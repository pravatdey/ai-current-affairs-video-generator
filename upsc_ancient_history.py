# UPSC Ancient History - 500 Questions (Prelims Pattern)
from fpdf import FPDF

class QuizPDF(FPDF):
    def __init__(self):
        super().__init__()
        self.set_auto_page_break(auto=True, margin=15)

    def header(self):
        self.set_font('Arial', 'B', 12)
        self.cell(0, 10, 'UPSC Ancient History - 500 MCQs (Prelims Pattern)', 0, 1, 'C')
        self.ln(3)

    def footer(self):
        self.set_y(-15)
        self.set_font('Arial', 'I', 8)
        self.cell(0, 10, f'Page {self.page_no()}', 0, 0, 'C')

    def add_question(self, q_num, question, options):
        self.set_font('Arial', 'B', 9)
        question = question.encode('latin-1', 'replace').decode('latin-1')
        self.multi_cell(0, 5, f"Q{q_num}. {question}")
        self.set_font('Arial', '', 8)
        for i, opt in enumerate(options):
            opt = opt.encode('latin-1', 'replace').decode('latin-1')
            self.cell(0, 4, f"    {chr(65+i)}) {opt}", 0, 1)
        self.ln(2)

